public class BazarPriveeDBCartItemModel {

    private String itemID;
    private String itemName;
    private String itemAge;
    private String itemEmail;
    private String itemGender;
    private String itemMobileNumber;


    public String getItemID() {
        return itemID;
    }

    public void setItemID(String itemID) {
        this.itemID = itemID;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getItemAge() {
        return itemAge;
    }

    public void setItemAge(String itemAge) {
        this.itemAge = itemAge;
    }

    public String getItemEmail() {
        return itemEmail;
    }

    public void setItemEmail(String itemEmail) {
        this.itemEmail = itemEmail;
    }

    public String getItemGender() {
        return itemGender;
    }

    public void setItemGender(String itemGender) {
        this.itemGender = itemGender;
    }

    public String getItemMobileNumber() {
        return itemMobileNumber;
    }

    public void setItemMobileNumber(String itemMobileNumber) {
        this.itemMobileNumber = itemMobileNumber;
    }
}
